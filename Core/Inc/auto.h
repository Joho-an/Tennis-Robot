#ifndef __AUTO_H
#define __AUTO_H

#include "main.h"

// ============================================================
//  停止条件阈值
// ============================================================
#define BATTERY_STOP_MV     10200  // 电池停机电压 (mV), 3S 约 10%
#define BALL_COUNT_MAX      20      // 最大拾取数量, 达到后停机

// ============================================================
//  自转找线参数
// ============================================================
#define SPIN_SPEED          120    // 自转差速 mm/s
#define SPIN_LINE_THRESH    8      // |offset| > 此值视为找到线
#define SPIN_LINE_FRAMES    8      // 连续 N 帧有线 → 确认找到
#define SPIN_TIMEOUT_MS     5000   // 自转超时 (ms)

// ============================================================
//  球追踪丢球超时保护 (防止路过球后卡死在旋转搜索)
// ============================================================
#define BALL_TRACK_TIMEOUT_MS 3000 // 丢球超过3秒 → 退回 LINE_FOLLOW

// ============================================================
//  球检测去抖
// ============================================================
#define BALL_SEEN_FRAMES    1      // 连续 N 帧有球 → 确认发现 (1=无去抖,即时切换)

// ============================================================
//  自动模式状态机
// ============================================================
typedef enum {
    AUTO_STATE_LINE_FOLLOW = 0,  // 自动循迹
    AUTO_STATE_BALL_TRACK,       // 追踪网球
    AUTO_STATE_GRAB,             // 抓取中 (等待舵机完成)
    AUTO_STATE_WAIT_LINE,        // 抓完等线 (原地停1秒看黑线)
    AUTO_STATE_SPIN_SEARCH,      // 自转找线
    AUTO_STATE_STOP              // 停机
} AutoState_t;

// ============================================================
//  抓取后等待看线参数
// ============================================================
#define WAIT_LINE_TIMEOUT_MS 1500  // 原地等线超时 (ms), 超时后自转找线
#define WAIT_LINE_FRAMES     5     // 连续 N 帧看到线 → 直接循迹

// ============================================================
//  函数声明
// ============================================================
void AutoMode_Run(void);           // 自动模式主循环 (main.c 中调用)
void AutoMode_Reset(void);         // 模式切换时重置
uint8_t AutoMode_GetBallCount(void); // 查询已抓球数 (LCD 显示用)

#endif /* __AUTO_H */
